

--FONKSIYON OLUSTRUMA ISLEMI

CREATE FUNCTION DBO.TOPLA(@SAYI1 AS INTEGER, @SAYI2 AS INTEGER)
RETURNS INTEGER
AS
BEGIN


RETURN @SAYI1 + @SAYI2

END


SELECT DBO.TOPLA (10,15)


--OLUSTURULAN FONKSIYONU DEGISTIRME

ALTER FUNCTION DBO.TOPLA(@SAYI1 AS INTEGER, @SAYI2 AS INTEGER)
RETURNS INTEGER
AS
BEGIN
DECLARE @SONUC AS INTEGER
SET @SONUC = @SAYI1 + @SAYI2

RETURN @SONUC
END
